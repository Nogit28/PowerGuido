    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        try {
            sql = conexao.prepareStatement("update usuario set nome=?,login=?,senha=?,email=?,cpf=? where login='"+user+"'");
            sql.setString(1, tfNome.getText());
            sql.setString(2, tfLogin.getText());
            sql.setString(3, tfSenha.getText());
            sql.setString(4, tfEmail.getText());
            sql.setString(5, tfCpf.getText());
            sql.executeUpdate();
            lbMsg.setText ("Alterado com sucesso!");
        } catch (SQLException ex) {
            Logger.getLogger(CadUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

