    private void btConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarActionPerformed
        try {
            tfNome.setText(null);
            tfSenha.setText(null);
            tfEmail.setText(null);
            tfCpf.setText(null);
            sql = conexao.prepareStatement("select * from usuario where login=?");
            sql.setString(1, tfLogin.getText());
            resultado = sql.executeQuery();
            if(resultado!=null && resultado.next()){
                tfSenha.setText(resultado.getString("senha"));
                tfNome.setText(resultado.getString("nome"));
                tfCpf.setText(resultado.getString("cpf"));
                tfEmail.setText(resultado.getString("email"));
                user = resultado.getString("login");
                lbMsg.setText(" ");
                btAlterar.setEnabled(true);
                btSalvar.setEnabled(false);
                btExcluir.setEnabled(true);
            } else {
                lbMsg.setText("Login inexistente!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btConsultarActionPerformed

