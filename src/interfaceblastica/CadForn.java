    private void btConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarActionPerformed
        try {
            tfNome.setText(null);
            tfEmail.setText(null);
            sql = conexao.prepareStatement("select * from fornecedor where cnpj=?");
            sql.setString(1, tfCNPJ.getText());
            resultado = sql.executeQuery();
            if(resultado!=null && resultado.next()){
                tfNome.setText(resultado.getString("nome"));
                tfEmail.setText(resultado.getString("email"));
                user = resultado.getString("cnpj");
                lbMsg.setText(" ");
                btAlterar.setEnabled(true);
                btSalvar.setEnabled(false);
                btExcluir.setEnabled(true);
            }else{
                lbMsg.setText("CPNJ inexistente!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadForn.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btConsultarActionPerformed

