    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        try {
            sql = conexao.prepareStatement("update fornecedor set nome=?,cnpj=?,email=? where cnpj='"+user+"'");
            sql.setString(1, tfNome.getText());
            sql.setString(2, tfCNPJ.getText());
            sql.setString(3, tfEmail.getText());
            sql.executeUpdate();
            lbMsg.setText ("Alterado com sucesso!");
        } catch (SQLException ex) {
            Logger.getLogger(CadForn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

